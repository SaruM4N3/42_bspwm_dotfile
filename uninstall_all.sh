#!/bin/bash
# =============================================================
#  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—в–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—
#  в•ҡв•җв•җв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ
#    в–Ҳв–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—
#   в–Ҳв–Ҳв–Ҳв•”в•қ  в•ҡв•җв•җв•җв•җв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•қ
#  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—
#  в•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•қ  в•ҡв•җв•җв•җв•қв•ҡв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ
#
#  uninstall_all.sh вҖ” full uninstall (packages + configs + junest)
#  Repo: https://github.com/SaruM4N3/42_bspwm_dotfile
# =============================================================

RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'; NC='\033[0m'
info()  { echo -e "${GREEN}[+]${NC} $*"; }
warn()  { echo -e "${YELLOW}[!]${NC} $*"; }

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
JUNEST_BIN="$HOME/.local/share/junest/bin/junest"
UNINSTALLER="$HOME/.bspwminstaller/uninstall_prerequisites_pacman.sh"

# в”Җв”Җ Confirmation в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
printf "\n%b\n" "${RED}[!]${NC} This will remove EVERYTHING installed by this dotfiles setup:"
printf "    packages, config files, junest, GNOME shortcut, and leftover files.\n"
printf "    Your dotfiles repository will NOT be deleted.\n"
printf "\nContinue? [y/N]: "
read -r ans
case "$ans" in
    y|Y) ;;
    *) echo "Aborted."; exit 0 ;;
esac

# в”Җв”Җ Step 1: Remove packages + config files (inside junest) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if [ -x "$JUNEST_BIN" ] && [ -f "$UNINSTALLER" ]; then
    info "Running package and config uninstall inside junest..."
    UNINSTALL_SKIP_CONFIRM=1 "$JUNEST_BIN" -- bash "$UNINSTALLER"
elif [ -f "$UNINSTALLER" ]; then
    warn "junest not found вҖ” running uninstaller directly (pacman commands may fail)..."
    UNINSTALL_SKIP_CONFIRM=1 bash "$UNINSTALLER"
else
    warn "Uninstaller script not found at $UNINSTALLER вҖ” skipping package removal."
fi

# в”Җв”Җ Step 2: Stop background daemons в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
info "Stopping bt-reconnect daemon..."
pkill -f bt-reconnect 2>/dev/null || true

# в”Җв”Җ Step 3: Remove junest environment в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
info "Removing junest Arch environment (~/.junest)..."
rm -rf "$HOME/.junest"

info "Removing junest binary (~/.local/share/junest)..."
rm -rf "$HOME/.local/share/junest"

# Remove junest wrapper bin if present
[ -d "$HOME/.junest/usr/bin_wrappers" ] && rm -rf "$HOME/.junest/usr/bin_wrappers"

# в”Җв”Җ Step 4: Remove GNOME keyboard shortcut в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if command -v dconf >/dev/null 2>&1; then
    info "Removing GNOME bspwm keyboard shortcut..."
    BASE="/org/gnome/settings-daemon/plugins/media-keys"
    NEW_KEY="custom5"
    NEW_DIR="$BASE/custom-keybindings/$NEW_KEY/"
    current=$(dconf read "$BASE/custom-keybindings" 2>/dev/null)
    if echo "$current" | grep -q "$NEW_KEY"; then
        new_list=$(echo "$current" | sed "s|, '$NEW_DIR'||;s|'$NEW_DIR', ||;s|'$NEW_DIR'||")
        [ "$new_list" = "[]" ] || [ -z "$new_list" ] && new_list="@as []"
        dconf write "$BASE/custom-keybindings" "$new_list"
        dconf reset "${NEW_DIR}name"
        dconf reset "${NEW_DIR}command"
        dconf reset "${NEW_DIR}binding"
        info "GNOME shortcut removed."
    else
        info "GNOME shortcut not found, skipping."
    fi
fi

# в”Җв”Җ Step 5: Leftover files в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
info "Removing leftover files..."
rm -f "$HOME/bspwm_launch.log"
rm -f "$HOME/.config/bspwm/config/.first_run_done"

# в”Җв”Җ Done в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo ""
info "Full uninstall complete."
warn "Your dotfiles repository at $SCRIPT_DIR has NOT been removed."
warn "You may delete it manually: rm -rf \"$SCRIPT_DIR\""
